    public void sectionVisible() {
        StringBuilder text = new StringBuilder();
        text.append(BUNDLE.getString("ResultsText"));
        text.append("<table>");
        for (TestSection section : TestManager.INSTANCE.getSections()) {
            if (section instanceof RunnableTestSection) {
                text.append("<tr>").append("<td>");
                text.append(TestsSection.resultImage(((RunnableTestSection) section).getResult()));
                text.append("</td><td><h2>");
                text.append(section.getName());
                text.append("</h2></td>").append("</tr>");
            }
        }
        text.append("</table>");
        
        textPane.setText(text.toString());
    }

